package com.lec.java.while02;

public class While02Main {

	public static void main(String[] args) {
		System.out.println("while 연습");
		
		int dan = 2;
		
		for(int i = 1; i <= 9; i++) {
			System.out.println(dan + " x " + i + " = " + (dan * i));
		}
		
		System.out.println("while 구구단");
		int i = 1;
		while(i <= 9) {
			
			System.out.println(dan + " x " + i + " = " + (dan * i));
			
			i++;
		} // end while
		
		


	} // end main()

} // end class While02Main









